
package Model;

public class Estudiante {
    private int id_est;
    private String cc_est;
    private String nom_est;
    private String ape_est;
    private String email_est;
    private String tel_est;

    public int getId_est() {
        return id_est;
    }

    public void setId_est(int id_est) {
        this.id_est = id_est;
    }

    public String getCc_est() {
        return cc_est;
    }

    public void setCc_est(String cc_est) {
        this.cc_est = cc_est;
    }

    public String getNom_est() {
        return nom_est;
    }

    public void setNom_est(String nom_est) {
        this.nom_est = nom_est;
    }

    public String getApe_est() {
        return ape_est;
    }

    public void setApe_est(String ape_est) {
        this.ape_est = ape_est;
    }

    public String getEmail_est() {
        return email_est;
    }

    public void setEmail_est(String email_est) {
        this.email_est = email_est;
    }

    public String getTel_est() {
        return tel_est;
    }

    public void setTel_est(String tel_est) {
        this.tel_est = tel_est;
    }
    
    
}
