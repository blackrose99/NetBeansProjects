
package Model;

public class Asignatura {
    private int id_asi;
    private String nom_asi;
    private int cre_asi;

    public int getId_asi() {
        return id_asi;
    }

    public void setId_asi(int id_asi) {
        this.id_asi = id_asi;
    }

    public String getNom_asi() {
        return nom_asi;
    }

    public void setNom_asi(String nom_asi) {
        this.nom_asi = nom_asi;
    }

    public int getCre_asi() {
        return cre_asi;
    }

    public void setCre_asi(int cre_asi) {
        this.cre_asi = cre_asi;
    }
    
    
}
